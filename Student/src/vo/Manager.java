package vo;
public class Manager {
	private String manno;   //管理员编号
	private String manname;   //管理员姓名
	private String manpwd;    //管理员密码
	public String getManno() {
		return manno;
	}
	public void setManno(String manno) {
		this.manno = manno;
	}
	public String getManname() {
		return manname;
	}
	public void setManname(String manname) {
		this.manname = manname;
	}
	public String getManpwd() {
		return manpwd;
	}
	public void setManpwd(String manpwd) {
		this.manpwd = manpwd;
	}	
}
